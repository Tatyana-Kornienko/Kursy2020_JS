function allStudentMarks(){
   	var allMarks = [].concat(...arguments);
    var levelStudents = sortMarks( allMarks);
    displayResults(levelStudents);
}

function sortMarks(data){
	var highLevel=[], middleLevel = [], lowLevel= [];
	for(let studentData of data){
        if(studentData.mark >= 89){
            levelStudents(studentData, highLevel)  
        }else if(studentData.mark >= 65 && studentData.mark < 89){
            levelStudents(studentData, middleLevel) 
        }else{
            levelStudents(studentData, lowLevel) 
        }
    }
    return [].concat([highLevel], [middleLevel], [lowLevel]) 
}

function levelStudents(studentData, array){
  return array.push(studentData)
}

function displayResults(levelStudents){
    var studentsList = document.querySelector("#students-list");
	for(let i = 0; i < levelStudents.length; i++){
        var blockHeader =  createElement("h1", "user-header", `<br> ${
            (i == 0)? "High Level" :
            (i == 1)? "Middle Level" : "Low Level"}`)
        
        let classDisplay = (i == 0)? "top-group" : (i == 1)? "middle-group" : "bottom-group";
        studentsList.appendChild(blockHeader);
        for(let j = 0; j < levelStudents[i].length; j++){
            let studentsDataFlow = levelStudents[i][j]
            let innerText = `<div>
                    ${studentsDataFlow.name} group number ${studentsDataFlow.groupId} 
                    <img src="${studentsDataFlow.photo}"> <mark>${studentsDataFlow.mark}</mark> 
                    <span>${studentsDataFlow.science}</span>        
                </div>`;
            let newStudentElement = createElement("li", `student-card ${classDisplay}`, innerText);
            studentsList.appendChild(newStudentElement);
        }
        
    }
}


function createElement(tag, className, innerText){
    var newObject = document.createElement(tag);
    newObject.className = (className)? className : false;
    newObject.innerHTML = (innerText)? innerText : false;

    return newObject
}